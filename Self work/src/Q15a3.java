import java.util.*;
public class Q15a3 {

	public static void main(String[] args) {
		System.out.println("Enter a number");
		Scanner sc = new Scanner(System.in);
		int x = sc.nextInt();
		if (x % 2 == 0) {
			System.out.println("even number");
		} else {
			System.out.println("odd number");
		}

	}

}
